//A web: protocolo

/* 
--Um protocolo e uma forma de comunicação entre computadores através da rede;

--O HTTP, serve para solicitar arquivos, imagens e etc (Hyper Text Transfer Protocol);

--SMTP = protocolos para enviar e-mail;

--TCP = protocolo de transferência;
*/

//A web: url

/* 
--Cada arquivo que carrega no navegador e nomeado por uma URL;

--A URL (Uniform Resource Locator) pode ser dividida em tres partes: protocolo, servidor e arquivo;

--http://www.website.com.br/index.html

--http protocolo;

--www.website.com.br = servidor (DNS para um IP);

--index.html = arquivo;
*/

//A web: HTML

/* 
--HTML (Hyper Text Markup Language) e uma linguagem de marcação;

--Responsável pelos textos e elementos que vemos na tela ao acessar uma pagina web;

--As tags do HTML criam: títulos, parágrafos, imagens, listas e etc...

--Todo documento HTML tem sempre duas partes importantes: head e body;

--O head serve para configurações do documento, como links de folha de estilo (css) ou ate scripts de Js;

--Tags de configuração: meta, link;

--O body para os elementos (tags), ou seja, a parte visual;

--Tags do body: p, h1, div, ul, li;
*/

//A web: HTML e o JS

/* 
--Pode-se utilizar Js no HTML por meio da tag script;

--Ou linkar um arquivo .js por meio de script com o atributo src;

--Algumas tags HTML aceitam atributos que executam JS, mas nao e muito utilizado;
*/

//A web: A estrutura HTML e o DOM

/* 
-Quando uma pagina web e requisitada, ela recebe um texto que e transformado em uma estrutura HTML;

--As tags vao se aninhando uma as outras, formando uma estrutura em que elementos ficam dentro dos outros;

--Essa estrutura tem uma representação exatamente igual para o Js chamada de DOM (Document Object Model);

--E por meio do Js pode-se acessar cada um destes elementos do HTML através do DOM;
*/

//A web: DOM

/* 
--Pode-se mudar completamente uma pag HTML através do DOM;

--E possível alterar: elementos, atributos, CSS;

--Além de alterar também e possível adicionar e remover;
*/